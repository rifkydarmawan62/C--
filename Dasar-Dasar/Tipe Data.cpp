int main(void){
    /*Tipe data char memiliki ukuran memori maksimum 8 bit atau 1 byte dan hanya dapat menyimpan sebuah karakter string*/
    char tipe_data_char;
    /*Tipe data short memiliki ukuran memori maksimum 16 bit atau 2 byte dan memiliki jangkauan bilangan dari -32768 sampai 32767*/
    short tipe_data_short; /*atau*/ short int tipe_data_short;
    /*Tipe data int memiliki ukuran memori maksimum 32 bit atau 4 byte dan memiliki jangkauan bilangan dari -2147483648 sampai 2147483647*/
    int tipe_data_int;
    /*Tipe data long memiliki ukuran memori maksimum 64 bit atau 8 byte dan memiliki jangkauan bilangan dari -9223372036854775808 sampai 9223372036854775807*/
    long tipe_data_long; /*atau*/ long int tipe_data_long;
    /*Tipe data float memiliki ukuran memori maksimum 32 bit atau 4 byte dengan presisi 7 digit desimal*/
    float tipe_data_float;
    /*Tipe data double memiliki ukuran memori maksimum 64 bit atau 8 byte dengan presisi 15 digit desimal*/
    double tipe_data_double;
    /*Tipe data unsigned hanya untuk mendeklarasikan bilangan positif saja*/
    unsigned int tipe_data_unsigned_int;
    return 0;
}